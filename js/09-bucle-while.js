'use strict'

// Bucle While // mientras se cumpla una condicion s

var year = 2018;

while(year <= 2051){
// Ejecuta esto
console.log("Estamos en el año:"+year);

// Operador de incremento
year++;
}

while(year != 1991){
    // Ejecuta esto
    console.log("Estamos en el año:"+year);
    
    // Operador de decremento
    year--;
}
